from re import A
from unittest import main
from langchain.agents import create_agent
from agent.tools.middleware import monitor_tool, log_before_model, report_prompt_switch
from agent.tools.agent_tools import (rag_summarize, get_weather, get_user_location, get_user_id, get_current_month,
                                     fetch_external_data, fill_context_for_report)
from model.factory import chat_model
from utils.prompt_loader import load_system_prompts


class ReactAgent:
    def __init__(self):
        self.agent = create_agent(
            model=chat_model,
            system_prompt=load_system_prompts(),
            tools=[rag_summarize, get_weather, get_user_location, get_user_id, get_current_month,
                                     fetch_external_data, fill_context_for_report],
            middleware=[monitor_tool, log_before_model, report_prompt_switch]
        )

    
    def execute_stream(self, query):
        input_dict = {
            "messages":[
                {"role":"user", "content":query }
            ]
        }
        # 执行流式输出
        for chunk in self.agent.stream(input_dict,
                          stream_mode='values',
                          context={"report":False} #上下文信息中注入report
                          ):
            latest_msg = chunk["messages"][-1]
            yield latest_msg.content.strip() + "\n"


if __name__ == '__main__':
    agent = ReactAgent()
    for chunk in agent.execute_stream("生成我的使用报告"):
        print(chunk,end='',flush=True)
